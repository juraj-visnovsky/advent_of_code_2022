import { readFileSync } from "fs";

import { produceImage } from "./part_two";

describe("part two", () => {
  test("example input", () => {
    const result = produceImage(readFileSync("./10/example_input.txt", "utf-8"));

    expect(result).toEqual(
      "##..##..##..##..##..##..##..##..##..##..\n" +
        "###...###...###...###...###...###...###.\n" +
        "####....####....####....####....####....\n" +
        "#####.....#####.....#####.....#####.....\n" +
        "######......######......######......####\n" +
        "#######.......#######.......#######.....",
    );
  });

  test("input", () => {
    const result = produceImage(readFileSync("./10/input.txt", "utf-8"));

    expect(result).toEqual(
      "###....##.####.###..#..#.###..####.#..#.\n" +
        "#..#....#.#....#..#.#..#.#..#.#....#..#.\n" +
        "###.....#.###..#..#.####.#..#.###..#..#.\n" +
        "#..#....#.#....###..#..#.###..#....#..#.\n" +
        "#..#.#..#.#....#.#..#..#.#.#..#....#..#.\n" +
        "###...##..#....#..#.#..#.#..#.#.....##..",
    );
  });
});
